#!/usr/bin/python3
# 4. Create a program that asks for two numbers,
#       and prints their sum, difference, product, and quotient.
x = int(input("Enter Your First Number to calculate: "))
y = int(input("Enter Your Second Number to calculate: "))
print(f'Sum: {x} + {y} = {x + y}')
print(f'Difference: {x} - {y} = {x - y}')
print(f'Product: {x} * {y} = {x * y}')
print(f'Quotient{x} // {y} = {x // y}')